print('\033[1;33m === EXERCÍCIO 016 === \033[m')
import math
num = float(input('\033[1mDigite um número real:'))
print('Os módulos números no número real {} estão indicados:'.format(num))
print('Arredondamento para cima:', math.ceil(num))
print('Arredondamento para baixo:', math.floor(num))
print('Truncado na sua parte inteira:', math.trunc(num))
print('Raiz quadrado do número:', math.sqrt(num))
print('Fatorial do valor:', math.factorial(int(num)))
print('\033[1;33m === FINALIZADO 016 ===')





